#!/usr/bin/env node
/*
 * Single source of truth: mcp/package.json (name, version, description, repo)
 *                         mcp/src/index.ts (tool list)
 *
 * This script regenerates the derived files so they stay in sync:
 *   - mcp/server.json          (Official MCP Registry manifest)
 *   - mcp/smithery.yaml        (Smithery directory manifest)
 *   - mcp/README.md            (tool count + tool catalogue between markers)
 *
 * Run automatically before `npm publish` via the prepublishOnly hook,
 * or manually any time you add/remove a tool: `npm run sync-metadata`.
 */
import { readFileSync, writeFileSync } from 'node:fs'
import { fileURLToPath } from 'node:url'
import { dirname, join } from 'node:path'

const here = dirname(fileURLToPath(import.meta.url))
const root = join(here, '..')

const pkg = JSON.parse(readFileSync(join(root, 'package.json'), 'utf8'))
const src = readFileSync(join(root, 'src/index.ts'), 'utf8')

const tools = [...new Set(src.match(/'karea_[a-z_]+'/g) || [])]
  .map(s => s.slice(1, -1))
  .sort()
const toolCount = tools.length

const baseDescription = `MCP server for Karea task management - ${toolCount} tools for Claude Code, Cursor, and other MCP clients to create, edit, close, and recap your dev tasks`

const serverJson = {
  $schema: 'https://static.modelcontextprotocol.io/schemas/2025-12-11/server.schema.json',
  name: pkg.mcpName,
  description: `Karea task manager MCP server - ${toolCount} tools so Claude Code, Cursor, and other MCP clients can create, edit, close, and recap your dev tasks while you code.`,
  status: 'active',
  repository: {
    url: pkg.repository.url,
    source: 'github'
  },
  version: pkg.version,
  websiteUrl: 'https://karea.app',
  packages: [
    {
      registryType: 'npm',
      identifier: pkg.name,
      version: pkg.version,
      transport: { type: 'stdio' },
      environmentVariables: [
        {
          name: 'KAREA_API_KEY',
          description: 'Your Karea API key. Generate at https://karea.app/dashboard/settings/api-keys',
          isRequired: true,
          isSecret: true
        },
        {
          name: 'KAREA_URL',
          description: 'Karea base URL. Defaults to https://karea.app for the hosted service.',
          isRequired: false,
          default: 'https://karea.app'
        }
      ]
    }
  ]
}
writeFileSync(join(root, 'server.json'), JSON.stringify(serverJson, null, 2) + '\n')

const smitheryYaml = `name: karea
displayName: Karea Task Manager
description: ${toolCount} tools so Claude Code, Cursor, and other MCP clients can manage your dev tasks (create, edit, close, recap, link to Jira, attach resources).
publisher: starecz
homepage: https://karea.app
repository: ${pkg.repository.url}
license: ${pkg.license}
categories:
  - productivity
  - task-management
  - developer-tools
keywords:
  - tasks
  - todo
  - claude-code
  - cursor
  - project-management
  - jira
startCommand:
  type: stdio
  configSchema:
    type: object
    required:
      - kareaApiKey
    properties:
      kareaApiKey:
        type: string
        title: Karea API Key
        description: Generate yours at https://karea.app/dashboard/settings/api-keys
        format: password
      kareaUrl:
        type: string
        title: Karea Base URL
        description: Defaults to the hosted service. Override only if self-hosting.
        default: https://karea.app
  commandFunction: |-
    (config) => ({
      command: 'npx',
      args: ['-y', '${pkg.name}'],
      env: {
        KAREA_API_KEY: config.kareaApiKey,
        KAREA_URL: config.kareaUrl || 'https://karea.app'
      }
    })
`
writeFileSync(join(root, 'smithery.yaml'), smitheryYaml)

const readmePath = join(root, 'README.md')
let readme = readFileSync(readmePath, 'utf8')
const toolListMd = tools.map(t => `- \`${t}\``).join('\n')
const catalogueBlock = `<!-- SYNC:TOOL_CATALOGUE -->
**${toolCount} tools** (regenerated by \`scripts/sync-metadata.mjs\`):

${toolListMd}
<!-- /SYNC:TOOL_CATALOGUE -->`
readme = readme.replace(
  /<!-- SYNC:TOOL_CATALOGUE -->[\s\S]*?<!-- \/SYNC:TOOL_CATALOGUE -->/,
  catalogueBlock
)
readme = readme.replace(
  /<!-- SYNC:TOOL_COUNT -->.*?<!-- \/SYNC:TOOL_COUNT -->/g,
  `<!-- SYNC:TOOL_COUNT -->${toolCount}<!-- /SYNC:TOOL_COUNT -->`
)
writeFileSync(readmePath, readme)

if (pkg.description !== baseDescription) {
  pkg.description = baseDescription
  writeFileSync(join(root, 'package.json'), JSON.stringify(pkg, null, 2) + '\n')
}

console.log(`[sync-metadata] wrote server.json, smithery.yaml, README.md (${toolCount} tools)`)
